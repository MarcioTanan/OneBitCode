let primeiroNumero = prompt("Digite o primeiro número: ")
let segundoNumero = prompt("Digite o segundo número: ")

let pnumero = parseFloat(primeiroNumero)
let snumero = parseFloat(segundoNumero)

let soma = pnumero + snumero
let subtracao = pnumero - snumero
let multiplicacao = pnumero * snumero
let divisao = pnumero / snumero

alert(`A soma dos números: ${soma}\nA subtracao dos números: ${subtracao}\nA multiplicacao dos números: ${multiplicacao}\nA divisao dos números: ${divisao}\n `)